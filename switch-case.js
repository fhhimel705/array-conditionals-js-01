const color = 'green';

switch(color){
    case 'black':
        console.log('color is black');
        break;
    case 'white':
        console.log('color is white');
        break;
    case 'blue':
        console.log('color is blue');
        break;
    case 'green':
        console.log('color is green');
        break;
    case 'black':
        console.log('color is black');
        break;
    case 'black':
        console.log('color is black');
        break;
}